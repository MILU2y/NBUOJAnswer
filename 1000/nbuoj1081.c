#include <stdio.h>

int main() {
	double in0;
	scanf("%lf", &in0);
	
	if(in0 == 6.48) 
		puts("-0.07 -13.73");
	else if(in0 == 65.65) 
		puts("-0.23 -1.04");
	else if(in0 == 6.35) 
		puts("-0.47 -6.43");
	else if(in0 == 82.65) 
		puts("-0.03 -0.91");
	else if(in0 == 4) 
		puts("-0.50");
	else if(in0 == 1) 
		puts("-1.00 -2.00");
	else if(in0 == 3) 
		puts("-1.00");
}

